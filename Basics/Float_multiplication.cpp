// This C++ code is to take two floating point numbers and print their multiplication.

#include <iostream>
using namespace std;

int main() {
    float number1;
    float number2;
    float result;

    cout << "Enter number 1: ";
    cin >> number1;

    cout << "Enter number 2: ";
    cin >> number2;

    result = number1 * number2;

    cout << "Multiplication result = " << result << endl;

    return 0;
}