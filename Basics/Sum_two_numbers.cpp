// This C++ code is to take two numbers from the user and print their sum.

#include <iostream>
using namespace std;

int main() {
    int Number1;
    int Number2;
    int sum;

    cout << "Enter number 1: ";
    cin >> Number1;

    cout << "Enter number 2: ";
    cin >> Number2;

    sum = Number1 + Number2;

    cout << "Sum = " << sum << endl;

    return 0;
}
